package Clases;

/*
 * Clase: User_Redes
 * Descripcion: Nombre de usuario en una o mas redes sociales
 */

public class User_Redes {

	private String user;
	private String red;

	public User_Redes(String user) {
		this.user = user;
		this.red = red;
	}

	public User_Redes() {

	}
	
	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getRed() {
		return red;
	}

	public void setRed(String red) {
		this.red = red;
	}

}

